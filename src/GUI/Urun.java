    private void uruntableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_uruntableMouseClicked
        int i = uruntable.getSelectedRow();
        tbxUrunIsmi.setText(model.getValueAt(i, 0).toString());
        tbxUrunSeriNo.setText(model.getValueAt(i, 1).toString());
        if (model.getValueAt(i, 2).equals("Mevcut")) {
            jRadioButton2.setSelected(true);
        } else if (model.getValueAt(i, 2).equals("Kayıp")) {
            jRadioButton3.setSelected(true);
        }


    }//GEN-LAST:event_uruntableMouseClicked
