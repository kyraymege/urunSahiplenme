    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int i = uruntable.getSelectedRow();
        if (i < 0) {
            JOptionPane.showMessageDialog(null, "Lütfen listeden ürünü seçiniz!");
        }
        model.removeRow(i);
        JOptionPane.showMessageDialog(null, "Ürün Başarıyla Silindi!");
        urun.dosyaSil("C:\\db\\urunler\\" + girisIslem.oturumKullaniciAdi() + ".txt");
        urun.dosyaYarat("C:\\db\\urunler\\" + girisIslem.oturumKullaniciAdi() + ".txt");
        for (int a = 0; a < uruntable.getRowCount(); a++) {
            urun.writeFile2(model.getValueAt(a, 0).toString(), model.getValueAt(a, 1).toString(), model.getValueAt(a, 2).toString(), model.getValueAt(a, 3).toString());

        }
        model.setRowCount(0);
        try (Scanner scan = new Scanner(new FileReader("C:\\db\\urunler\\" + girisIslem.oturumKullaniciAdi() + ".txt"));) {
            while (scan.hasNextLine()) {

                String kullanıcıBilgileri = scan.nextLine();
                String[] girisArray = kullanıcıBilgileri.split(",");
                model.addRow(girisArray);
            }
        } catch (FileNotFoundException ex) {

            Logger.getLogger(Urun.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

